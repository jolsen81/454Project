    private BigInteger calculate(int l){
        BigInteger[][] transToL = aToLPow(l);
        BigInteger[] uMultT =  uMultA(transToL);
        return uAV(uMultT);
    }
    private BigInteger uAV(BigInteger[] u){
        BigInteger res = new BigInteger("0");
        for(int i = 0; i < u.length; i++){
            res = res.add(u[i].multiply(this.accStatesBI[i]));
        }
        return res;
    }
    private BigInteger[] uMultA(BigInteger[][] A){
        BigInteger[] res = new BigInteger[this.startStatesBI.length];
        for(int j = 0; j < A.length; j++){
            BigInteger prod = new BigInteger("0");
            for(int k = 0; k < A.length; k++){
                prod = prod.add(this.startStatesBI[k].multiply(A[k][j]));
            }
            res[j] = prod;
        }
        return res;
    }
    private BigInteger[][] aToLPow(int l){
        if(l == 1){
            return this.transMatBI;
        }
        
        BigInteger[][] res = this.aToLPow(l/2);
        res = this.nByNMatMult(res, res);
        if(l%2 == 0){
            return res;
        }
        else{            
            return this.nByNMatMult(res, this.transMatBI);
        }
    }
    
    private BigInteger[][] nByNMatMult(BigInteger[][] a, BigInteger[][] b){
        BigInteger[][] res = new BigInteger[a.length][b.length];
                for(int k = 0; k < res.length; k++){
                        for(int m = 0; m < res.length; m++){
                             BigInteger prod = new BigInteger("0");
                           for(int n = 0; n < res.length; n++){
                                prod = prod.add(a[k][n].multiply(b[n][m]));
                           } 
                           res[k][m] = prod;
                        }
                }        
        return res;
    }
    
